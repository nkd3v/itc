boolean timePaused = false;
int eeAddress = 0;

struct Clock {
  unsigned long time;
  unsigned long alarmTime[5];
  int nMaxAlarm = 5;
  byte nAlarmSet;
} clock;

unsigned long prevPrintTime;
unsigned long printDuration;
boolean printLocked() { return millis() - prevPrintTime < printDuration; }

unsigned long prevDrawTime;
unsigned long drawDuration;
boolean drawLocked() { return millis() - prevDrawTime < drawDuration; }

void printToDisplay(const char* txt, int x, int y, unsigned long duration = 0) {
  if (printLocked() || drawLocked()) return;

  prevPrintTime = millis();
  printDuration = duration;

  OLED.clearDisplay();
  OLED.setCursor(x, y);
  OLED.println(txt);
  OLED.display();
}

void drawToDisplay(const unsigned char* bitmap, int x, int y, unsigned long duration = 0) {
  if (drawLocked() || printLocked()) return;

  prevDrawTime = millis();
  drawDuration = duration;

  OLED.clearDisplay();
  OLED.drawBitmap(x, y, bitmap, 128, 32, 1);
  OLED.display();
}

void displayTime(unsigned long time) {
  unsigned long sec = (time % 3600) % 60;
  unsigned long min = (time % 3600) / 60;
  unsigned long hour = time / 3600;

  char timeStr[10];
  sprintf(timeStr, "%02ld:%02ld:%02ld", hour, min, sec);

  printToDisplay(timeStr, 15, 10);
}

boolean alarmActive = false;

void playAlarm(unsigned long startTime) {
  if ((millis() - startTime) % 400 > 200) {
    printToDisplay("Alarmed!", 15, 10);
    tone(speakerPin, beepFreq);
  } else {
    printToDisplay("", 15, 10);
    noTone(speakerPin);
  }
}

void checkAlarm() {
  static unsigned int alarmStartTime;
  if (!alarmActive) {
    for (int i = 0; i < clock.nAlarmSet; i++) {
      if (clock.time == clock.alarmTime[i]) {
        alarmActive = true;
        alarmStartTime = millis();
      }
    }
  } else {
    playAlarm(alarmStartTime);
  }
}

void updateTime() {
  clock.time++;

  if (clock.time % 60 == 0)
    EEPROM.put(eeAddress, clock);
}

void updateDisplayConfig() {

  if (analogRead(ldrPin) > 480) {
    OLED.dim(true);
  } else {
    OLED.dim(false);
  }

  float xAccel = (analogRead(xPin) - 507) / 112.;
  
  if (xAccel > 0.4) {
    OLED.setRotation(0);
  } else if (xAccel < -0.4) {
    OLED.setRotation(2);
  }

}

void updateState() {

  static enum STATE {
    DISPLAY_TIME, SET_TIME, ALARM_MENU,
    STOPWATCH, MENU,
    ADD_ALARM, REMOVE_ALARM
  } currentState;

  switch (currentState) {

    case DISPLAY_TIME: {
      checkAlarm();

      if (alarmActive) {
        if (btnL.isPressed() || btnM.isPressed() || btnR.isPressed()) {
          alarmActive = false;
          noTone(speakerPin);
        }
        break;
      }

      if (btnM.isPressed()) {
        drawToDisplay(menu, 0, 0, 500);
        currentState = MENU;
        break;
      }

      displayTime(clock.time);

      break;
    }

    case MENU: {
      static int selectedOption = 0;
      const char* modeName[] = { "Show Time", "Set Time", "Set Alarm", "Stopwatch" };
      const size_t nMode = sizeof(modeName) / sizeof(modeName[0]);

      if (btnL.isPressed()) {
        currentState = DISPLAY_TIME;
        selectedOption = 0;
        break;
      }

      if (btnM.isPressed()) {
        currentState = (STATE)selectedOption;
        selectedOption = 0;
        break;
      }

      if (btnR.isPressed()) {
        selectedOption = (selectedOption + 1) % nMode;
      }
      
      printToDisplay(modeName[selectedOption], 15, 10);

      break;
    }

    case SET_TIME: {
      static boolean firstInit = true;
      static unsigned long timeToSet;
      if (firstInit) {
        timeToSet = clock.time;
        firstInit = false;
      }

      static enum { HOUR, MIN, SEC } setMode = HOUR;

      if (btnL.isPressed()) {
        if (setMode == HOUR) {
          currentState = MENU;
          firstInit = true;
        } else if (setMode == MIN) {
          setMode = HOUR;
        } else if (setMode == SEC) {
          setMode = MIN;
        }
      }

      unsigned long hour = timeToSet / 3600;
      unsigned long min = (timeToSet % 3600) / 60;
      unsigned long sec = timeToSet % 60;

      if (btnM.isPressed()) {
        if (setMode == HOUR)
          setMode = MIN;
        else if (setMode == MIN)
          setMode = SEC;
        else if (setMode == SEC) {
          clock.time = timeToSet;
          EEPROM.put(eeAddress, clock);

          setMode = HOUR;
          currentState = DISPLAY_TIME;
          firstInit = true;

          tone(speakerPin, successFreq, 200);
          printToDisplay("Time set!", 15, 10, 1000);
          break;
        }
      }

      if (btnR.isPressed()) {
        if      (setMode == HOUR) hour = (hour + 1) % 24;
        else if (setMode == MIN)  min = (min + 1) % 60;
        else if (setMode == SEC)  sec = (sec + 1) % 60;
      }

      timeToSet = hour * 3600 + min * 60 + sec;

      displayTime(timeToSet);

      if (setMode == HOUR) {
        OLED.drawLine(15, 28, 36, 28, WHITE);
      } else if (setMode == MIN) {
        OLED.drawLine(51, 28, 72, 28, WHITE);
      } else if (setMode == SEC) {
        OLED.drawLine(87, 28, 108, 28, WHITE);
      }

      OLED.display();

      break;
    }

    case ALARM_MENU: {
      int nOptions = clock.nAlarmSet > 0 ? 2 : 1;
      static int selectedOption = 0;
      const char* modeName[] = { "Add", "Remove" };

      if (btnL.isPressed()) {
        currentState = MENU;
        selectedOption = 0;
        break;
      }

      if (btnM.isPressed()) {
        if (selectedOption == 0) {
          if (clock.nAlarmSet == clock.nMaxAlarm) {
            tone(speakerPin, failFreq, 200);
            printToDisplay("MemFulled!", 5, 10, 1000);
            break;
          }
          
          currentState = ADD_ALARM;
        } else if (selectedOption == 1) {
          currentState = REMOVE_ALARM;
        }

        selectedOption = 0;
        break;
      }

      if (btnR.isPressed()) {
        selectedOption = (selectedOption + 1) % nOptions;
      }
      
      printToDisplay(modeName[selectedOption], 15, 10);

      break;
    }

    case ADD_ALARM: {
      static enum { HOUR, MIN, SEC } setMode = HOUR;
      static unsigned long alarmSetTime = 0;

      if (btnL.isPressed()) {
        if (setMode == HOUR) {
          currentState = ALARM_MENU;
          alarmSetTime = 0;
          setMode = HOUR;
          break;
        } else if (setMode == MIN) {
          setMode = HOUR;
        } else if (setMode == SEC) {
          setMode = MIN;
        }
      }

      if (btnM.isPressed()) {
        if (setMode == HOUR)
          setMode = MIN;
        else if (setMode == MIN)
          setMode = SEC;
        else if (setMode == SEC) {
          clock.alarmTime[clock.nAlarmSet] = alarmSetTime;
          clock.nAlarmSet++;

          setMode = HOUR;
          alarmSetTime = 0;

          currentState = DISPLAY_TIME;

          tone(speakerPin, successFreq, 200);
          printToDisplay("Alarm Set!", 7, 10, 1000);
          break;
        }
      }

      unsigned long hour = alarmSetTime / 3600;
      unsigned long min = (alarmSetTime % 3600) / 60;
      unsigned long sec = alarmSetTime % 60;

      if (btnR.isPressed()) {
        if      (setMode == HOUR) hour = (hour + 1) % 24;
        else if (setMode == MIN)  min = (min + 1) % 60;
        else if (setMode == SEC)  sec = (sec + 1) % 60;
      }

      alarmSetTime = hour * 3600 + min * 60 + sec;

      displayTime(alarmSetTime);

      if (setMode == HOUR) {
        OLED.drawLine(15, 28, 36, 28, WHITE);
      } else if (setMode == MIN) {
        OLED.drawLine(51, 28, 72, 28, WHITE);
      } else if (setMode == SEC) {
        OLED.drawLine(87, 28, 108, 28, WHITE);
      }

      OLED.display();

      break;
    }
    
    case REMOVE_ALARM: {
      static int selectedOption = 0;

      if (btnL.isPressed()) {
        selectedOption = 0;
        currentState = ALARM_MENU;
        break;
      }

      if (btnM.isPressed()) {
        for (int i = selectedOption; i < clock.nAlarmSet - 1; i++) {
          clock.alarmTime[i] = clock.alarmTime[i + 1];
        }
        clock.nAlarmSet--;
        EEPROM.put(eeAddress, clock);

        tone(speakerPin, successFreq, 200);
        printToDisplay("Removed!", 10, 10, 1000);

        selectedOption = 0;
        currentState = DISPLAY_TIME;
        break;
      }

      if (btnR.isPressed() && clock.nAlarmSet > 1) {
        selectedOption = (selectedOption + 1) % clock.nAlarmSet;
      }

      displayTime(clock.alarmTime[selectedOption]);

      break;
    }

    case STOPWATCH: {
      static boolean firstInit = true;

      static boolean stopwatchPaused = true;
      static unsigned long stopwatchTotalTime = 0;

      static unsigned long prevTime = millis();

      if (firstInit) {
        stopwatchPaused = true;
        prevTime = millis();
        firstInit = false;
      }
      
      unsigned long deltaTime = millis() - prevTime;
      prevTime = millis();

      if (!stopwatchPaused) {
        stopwatchTotalTime += deltaTime;
      }

      if (btnL.isPressed()) {
        currentState = MENU;
        firstInit = true;
        break;
      }

      if (btnM.isPressed()) {
        stopwatchPaused = true;
        stopwatchTotalTime = 0;
      }

      if (btnR.isPressed()) {
        stopwatchPaused = !stopwatchPaused;
      }

      displayTime(stopwatchTotalTime * 60 / 1000);
    }

    default:
      break;
  }
  
}

void setup() {
  
  Serial.begin(9600);

  if(!OLED.begin(SSD1306_SWITCHCAPVCC, SCREEN_ADDRESS)) {
    Serial.println(F("SSD1306 allocation failed"));
    for(;;);
  }

  pinMode(speakerPin, OUTPUT);

  btnL.begin();
  btnM.begin();
  btnR.begin();
  
  clock = Clock();
  EEPROM.put(eeAddress, clock);

  Timer1.initialize(1E6);
  Timer1.attachInterrupt(updateTime);

  drawToDisplay(shark, 0, 0, 2000);

  OLED.setTextColor(WHITE);
  OLED.setTextSize(2);
  OLED.setRotation(2);
}

void loop() {

  updateDisplayConfig();
  updateState();

}